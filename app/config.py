"""
Конфигурация приложения.

Все настройки берутся из переменных окружения (.env).
Переменные окружения — безопасный способ хранить секреты:
они не попадают в git, легко меняются между окружениями (dev/prod).
"""

import os
import secrets

from dotenv import load_dotenv

# Загружает переменные из файла .env в корне проекта.
# После этого они доступны через os.getenv().
load_dotenv()

# ──────────────────────────────────────────────
# База данных
# ──────────────────────────────────────────────
# Строка подключения к PostgreSQL.
# Формат: postgresql://пользователь:пароль@хост:порт/имя_базы
# В Docker хост = "postgres" (имя сервиса в docker-compose.yml).
# Локально = "localhost".
DATABASE_URL = os.getenv(
    "DATABASE_URL", "postgresql://postgres:postgres@localhost:5432/spotify_clone"
)

# ──────────────────────────────────────────────
# JWT (токены аутентификации)
# ──────────────────────────────────────────────
# SECRET_KEY — секрет для подписи JWT-токенов.
# Если ключ утечёт — злоумышленник сможет создавать поддельные токены.
# В production ОБЯЗАТЕЛЬНО задать через переменную окружения.
# Fallback secrets.token_urlsafe(32) генерирует случайный ключ —
# это небезопасно для prod (ключ меняется при рестарте, все токены инвалидируются).
SECRET_KEY = os.getenv("SECRET_KEY", secrets.token_urlsafe(32))

# HS256 — алгоритм подписи JWT (HMAC + SHA-256, симметричный: один ключ для создания и проверки).
ALGORITHM = "HS256"

# Access token живёт 30 минут — если украден, урон ограничен.
ACCESS_TOKEN_EXPIRE_MINUTES = 30

# Refresh token живёт 7 дней — используется только для получения нового access token.
REFRESH_TOKEN_EXPIRE_DAYS = 7

# ──────────────────────────────────────────────
# Права администратора
# ──────────────────────────────────────────────
# Список email-адресов, которые считаются администраторами.
# Задаётся через переменную окружения ADMIN_EMAILS (через запятую).
# Только администраторы могут создавать/редактировать/удалять треки и альбомы.
# Хранится как set — O(1) проверка принадлежности.
_admin_raw = os.getenv("ADMIN_EMAILS", "test@example.com")
ADMIN_EMAILS = {
    e.strip().lower()
    for e in _admin_raw.split(",")
    if e.strip()
}
# Страховка: если после парсинга список пуст — добавляем дефолтный email
if not ADMIN_EMAILS:
    ADMIN_EMAILS = {"test@example.com"}

# ──────────────────────────────────────────────
# Jamendo API
# ──────────────────────────────────────────────
# client_id для Jamendo API (бесплатно для некоммерческих проектов).
# Зарегистрировать приложение и получить свой ключ: https://devportal.jamendo.com
# b6747d04 — публичный демо-ключ для разработки и тестирования.
JAMENDO_CLIENT_ID = os.getenv("JAMENDO_CLIENT_ID", "b6747d04")

# ──────────────────────────────────────────────
# Кэш рекомендаций
# ──────────────────────────────────────────────
# Время жизни кэша рекомендаций в секундах.
# 0 = кэш отключён (каждый запрос пересчитывает рекомендации).
# 300 = кэш живёт 5 минут — баланс между актуальностью и нагрузкой на БД.
RECOMMENDATION_CACHE_TTL = int(os.getenv("RECOMMENDATION_CACHE_TTL", "300"))
